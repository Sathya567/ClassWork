using System;
using System.Collections.Generic;
using System.Text;

namespace ClassWork
{
    class PrintPattern5
    {
        static void Main(String[] args)
        {
            int i, j;
            for (i = 0; i <= 4; i++)
            {
                for (j = 0; j <= 7; j++)
                {
                    Console.Write("*");
                }
                Console.WriteLine(" ");
            }
            Console.ReadLine();
        }
    }
}
    

   
